//string data type
let Name = "charan";
console.log("Name:",Name);

//number data type
let age = 25;
console.log("Age:", age);

//boolean data type
let student = true;
console.log("Student:", student);

//undefined data type
let not_assigned;
console.log("Not Assigned:", not_assigned);

//null data type
let empty_value = null;
console.log("Empty value:", empty_value);

//symbol data type
let unique_symbol = Symbol('id');
console.log("Unique symbol:", unique_symbol);

//object data type
let person = {name: "charan",age: 21,Student: true};
console.log("Person:", person);

//array
let fruits = ["apple", "banana", "cherry"];
console.log("fruits:", fruits);